        switch varargin{2}
            
            case {1,2,3,4,5,6,7,8,11,12,13,14,15,16,56}
                value_strings{varargin{2}} = value_string;
                if(~isempty(hnd_this_figure))
                    set(value_handles(varargin{2}),'String',value_string);
                end
                
            case {17,18,19,20,21,55}
                colors{varargin{2}} = color;
                if(~isempty(hnd_this_figure))
                    set(value_handles(varargin{2}),'BackgroundColor',color);
                end  
                
            case {23,25,26,28,29,31,32,34}
                
