            if (isnan(varargin{3}))
                value_string = '---';
            elseif (abs(varargin{3}) < 1000)
                value_string = sprintf('%0.1f Hz',varargin{3});
            else
                value_string = sprintf('%0.3f kHz',varargin{3}/1000);
