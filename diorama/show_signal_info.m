                            set(value_handles(35:35 + min(rms_green, new_rms_bars) - 1),'BackgroundColor',[0 1 0]);
                        end
                        if (new_rms_bars > rms_green)
                            [colors{35 + rms_green:35 + min(rms_green + rms_yellow, new_rms_bars) - 1}]= deal([1 1 0]);
