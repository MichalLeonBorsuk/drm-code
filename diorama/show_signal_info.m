                                set(value_handles(35 + rms_green:35 + min(rms_green + rms_yellow, new_rms_bars) - 1),'BackgroundColor',[1 1 0]);
                            end
                        end
                        if (new_rms_bars > rms_green + rms_yellow)
                            [colors{35 + rms_green + rms_yellow:35 + new_rms_bars - 1}] = deal([1 0 0]);
                            if(~isempty(hnd_this_figure))
                                set(value_handles(35 + rms_green + rms_yellow:35 + new_rms_bars - 1),'BackgroundColor',[1 0 0]);
                            end
                        end
                        
                        rms_bars = new_rms_bars;
                end
                
            case 47
                if (frame_no >= 0)
                    if(~isempty(hnd_this_figure))
                        set(value_handles(47 + frame_no),'BackgroundColor',bg_color);
                    end
                end
