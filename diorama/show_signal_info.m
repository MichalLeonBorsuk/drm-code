            'ListboxTop',0, ...
            'Position',[0 0.48 0.43 0.51], ...
            'String','FAC/SDC data', ...
            'Style','frame', ...
            'TooltipString','FAC/SDC data');
        h1 = uicontrol('Parent',hnd_this_figure, ...
