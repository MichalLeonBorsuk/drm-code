    colors = cell(1,num_of_items);
    [colors{:}] = deal(bg_color);
    rms_bars = 0;
    frame_no = -1;
    warn_low_level = 0;
    blink_on = 0;
end


switch varargin{1}
    
    
case 2,
    
    
    update = 1;
    
    switch varargin{2}
        
        case 1, 
            value_string = varargin{3};
        case 2,
            value_string = sprintf('%i kHz',varargin{3});
        case 3,
            if (varargin{3} == 0), value_string = '2.0 s (long)'; else value_string = '400 ms (short)'; end
        case {4,5}
            value_string = varargin{3};
        case 6,
            plevels = varargin{3};
            if (plevels(1) < 0) value_string = sprintf('-/%i (EEP)',plevels(2)); else value_string = sprintf('%i/%i (UEP)',plevels(1),plevels(2)); end
        case 7,
            services = varargin{3};
            value_string = sprintf('%i Audio, %i Data',services(1),services(2));
        case 8,
            time_and_date = varargin{3};
            if (~isequal(time_and_date_old, time_and_date))
                if (time_and_date.day > 0)
                    value_string = sprintf('%02d:%02d %02d-%02d-%02d\n', time_and_date.hours, time_and_date.minutes, mod(time_and_date.year,100), time_and_date.month, time_and_date.day);
                else
                    value_string = '---';
                end
            else
                update = 0;
            end
            
            time_and_date_old = time_and_date;
            
        case 10,
            
            if (varargin{3} == 0 || isnan(varargin{3}))
                new_rms_bars = 0;
            else
                new_rms_bars = floor(log(varargin{3}*5000) / log(rms_max * 5000) * rms_total);
            end
            if (new_rms_bars > rms_total) new_rms_bars = rms_total; end
            if (new_rms_bars < 0) new_rms_bars = 0; end
            
