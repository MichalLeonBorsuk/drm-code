function result = show_signal_info( varargin )
% (-1) close figure and reset figure properties, result should be zero
% (0) close figure, result should be zero
% (1) create empty figure without data if not allready open, result==handle to figure
% (2, ...) update/create figure with data in the following arguments, result==handle to figure
%
% (10) put window into foreground
% (11) put window into background
% (12) put minimize figure
% (13, [xpos,ypos,xwidth,ywidth]) == set position of actual window or of position window will have if created
% (14) get position of actual window or last opened window, empty if not available


% Number    Meaning
% 1         DRM mode
% 2         Bandwidth
% 3         interleaver size
% 4         SDC mode 
% 5         MSC mode
% 6         protection levels
% 7         services
% 8         time and date
% 10        input rms
% 11        frequency offset
% 12        sampling rate offset
% 13        FAC MER
% 14        FAC WMER
% 15        MSC MER
% 16        MSC WMER
% 17        MSC CRC
% 18        SDC CRC
% 19        FAC CRC
% 20        frame sync
% 21        time sync
% 23        Service 1: label and type
% 25        Service 1: text message
% 26        Service 2: label and type
% 28        Service 2: text message
% 29        Service 3: label and type
% 31        Service 3: text message
% 32        Service 4: label and type
% 34        Service 4: text message
% 47        running
% 55        play buffer
% 56        audio frame error rate


persistent position_this_figure ...		%position of figure
   hnd_this_figure ...
   is_in_use ...
   ...
   ...	%----------change from here------------------------------
   text_handles value_handles value_strings colors time_and_date_old rms_bars frame_no warn_low_level blink_on;
   settings_number = 20;
   
   
   num_of_items = 56;
   
   num_of_frames = 8;
   
   rms_max = 0.5;
   rms_total = 12;
   rms_green = 7;
   rms_yellow = 3;
   
   bg_color = [0.8 0.8 0.8]; 
   frame_color = [0.7 0.7 0.7];  
   value_fontweight = 'bold';
   services_value_fontweight = 'bold';
   text_font_weight = 'normal';
   
   %------------dont change begin ------------
TAG_NAME = mfilename;
FIGURE_PROCEDURE_NAME = mfilename;

result=-1;
%avoid reentrance 
if ( ~isempty(is_in_use) )
   return; %ignore this call
end

is_in_use = varargin{1};

if( isempty(hnd_this_figure) )
   delete(findobj('Tag', TAG_NAME));
   
end

if( isempty(position_this_figure) )
   myScreenSize = get(0,'ScreenSize');
   position_this_figure = [10, myScreenSize(4) - 340, 450, 300];
end

if( isempty(value_strings) )
