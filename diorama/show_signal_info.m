                        [colors{35 + new_rms_bars:35 + rms_total - 1}] = deal(bg_color);
                        if(~isempty(hnd_this_figure))
                            set(value_handles(35 + new_rms_bars:35 + rms_total - 1),'BackgroundColor',bg_color);
                        end
